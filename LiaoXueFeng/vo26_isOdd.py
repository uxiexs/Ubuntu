#*******************************************************************************
# 把一个序列中的空字符串删掉
#*******************************************************************************
def is_odd(n):
    return n % 2 == 1

n = int(input('请输入要查看的奇数范围:'))
print(list(filter(is_odd, [x for x in range(n)])))
