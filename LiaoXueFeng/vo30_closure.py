#*******************************************************************************
# 闭包时牢记的一点就是：返回函数不要引用任何循环变量，或者后续会发生变化的变量。
#*******************************************************************************

def count(i):
    def f():
        return i*i
    return f
n = input("求平方 请输入数字:")
r = count(int(n))
print(r())
